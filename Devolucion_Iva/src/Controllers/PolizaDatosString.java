public class PolizaDatosString {

    private String numeroPoliza, ejercicio, fechaPoliza, conceptoPoliza, documentos;

    public String getNumeroPoliza() {
        return numeroPoliza;
    }

    public void setNumeroPoliza(String numeroPoliza) {
        this.numeroPoliza = numeroPoliza;
    }

    public String getEjercicio() {
        return ejercicio;
    }

    public void setEjercicio(String ejercicio) {
        this.ejercicio = ejercicio;
    }

    public String getFechaPoliza() {
        return fechaPoliza;
    }

    public void setFechaPoliza(String fechaPoliza) {
        this.fechaPoliza = fechaPoliza;
    }

    public String getConceptoPoliza() {
        return conceptoPoliza;
    }

    public void setConceptoPoliza(String conceptoPoliza) {
        this.conceptoPoliza = conceptoPoliza;
    }

    public String getDocumentos() {
        return documentos;
    }

    public void setDocumentos(String documentos) {
        this.documentos = documentos;
    }
    
}
