package Models;

/**
 *
 * @author Vigilancia
 */
public class RelacionActividades {

    private String idRelacion, codigoRelacion, descripcionRelacion;
    private String rfcRelacion, idAsociado, idRfc;

    public String getIdRelacion() {
        return idRelacion;
    }

    public void setIdRelacion(String idRelacion) {
        this.idRelacion = idRelacion;
    }

    public String getCodigoRelacion() {
        return codigoRelacion;
    }

    public void setCodigoRelacion(String codigoRelacion) {
        this.codigoRelacion = codigoRelacion;
    }

    public String getDescripcionRelacion() {
        return descripcionRelacion;
    }

    public void setDescripcionRelacion(String descripcionRelacion) {
        this.descripcionRelacion = descripcionRelacion;
    }

    public String getRfcRelacion() {
        return rfcRelacion;
    }

    public void setRfcRelacion(String rfcRelacion) {
        this.rfcRelacion = rfcRelacion;
    }

    public String getIdAsociado() {
        return idAsociado;
    }

    public void setIdAsociado(String idAsociado) {
        this.idAsociado = idAsociado;
    }

    public String getIdRfc() {
        return idRfc;
    }

    public void setIdRfc(String idRfc) {
        this.idRfc = idRfc;
    }

}
